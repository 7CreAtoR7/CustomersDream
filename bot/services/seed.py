async def seed_initial_data(session: AsyncSession) -> None:
    """Создаёт категории и подкатегории, если их ещё нет.

    Не создаёт макеты: их добавит админ через бота.
    """
    existing_count = await session.scalar(select(Category.id).limit(1))
    if existing_count is not None:
        return

    for position, (emoji, name, subs) in enumerate(CATALOG):
        cat = Category(name=name, emoji=emoji, position=position)
        session.add(cat)
        await session.flush()
        for sub_pos, sub_name in enumerate(subs):
            session.add(
                Subcategory(
                    category_id=cat.id, name=sub_name, position=sub_pos
                )
            )
    await session.commit()
